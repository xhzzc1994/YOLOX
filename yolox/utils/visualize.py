def vis(img, boxes, scores, cls_ids, txt_save_path, conf=0.5, class_names=None):
    for i in range(len(boxes)):
        box = boxes[i]
        cls_id = int(cls_ids[i])
        score = scores[i]
        if score < conf:
            continue
        x0 = int(box[0])
        y0 = int(box[1])
        x1 = int(box[2])
        y1 = int(box[3])

        # ---- 标注格式使用此部分 ----
        bbox_width = x1 - x0
        bbox_height = y1 - y0
        bbox_center_x = x0 + bbox_width / 2
        bbox_center_y = y0 + bbox_height / 2
        bbox_width = format((bbox_width / 1920), '.6f')
        bbox_height = format((bbox_height / 1080), '.6f')
        bbox_center_x = format((bbox_center_x / 1920), '.6f')
        bbox_center_y = format((bbox_center_y / 1080), '.6f')
        curLine = [str(cls_id), bbox_center_x, bbox_center_y, bbox_width, bbox_height]
        curLine.insert(0, " ")
        curLine.insert(2, " ")
        curLine.insert(4, " ")
        curLine.insert(6, " ")
        curLine.insert(8, " ")
        strLine = "".join(curLine)
        # ---- 带有score使用此部分 ----
        # curLine = [str(cls_id), str(x0), str(y0), str(x1), str(y1), str(float(score))]
        # print(curLine)
        # curLine.insert(0, " ")
        # curLine.insert(2, " ")
        # curLine.insert(4, " ")
        # curLine.insert(6, " ")
        # curLine.insert(8, " ")
        # curLine.insert(10, " ")
        # strLine = "".join(curLine)

        color = (_COLORS[cls_id] * 255).astype(np.uint8).tolist()
        text = '{}:{:.1f}%'.format(class_names[cls_id], score * 100)
        txt_color = (0, 0, 0) if np.mean(_COLORS[cls_id]) > 0.5 else (255, 255, 255)
        font = cv2.FONT_HERSHEY_SIMPLEX

        txt_size = cv2.getTextSize(text, font, 0.4, 1)[0]
        cv2.rectangle(img, (x0, y0), (x1, y1), color, 2)

        txt_bk_color = (_COLORS[cls_id] * 255 * 0.7).astype(np.uint8).tolist()
        cv2.rectangle(
            img,
            (x0, y0 + 1),
            (x0 + txt_size[0] + 1, y0 + int(1.5 * txt_size[1])),
            txt_bk_color,
            -1
        )
        cv2.putText(img, text, (x0, y0 + txt_size[1]), font, 0.4, txt_color, thickness=1)
        # 保存检测结果到txt文档
        with open(txt_save_path, 'a') as f:
            f.write(strLine)
            f.write("\n")

    return img


